def test_2_list_includes_field(sagar_uid):
    print("\n=== Test 2: GET /api/admin/primary-owners includes dashboard_full_access ===")
    r = requests.get(f"{API}/admin/primary-owners", headers=H(SO_TOKEN), timeout=15)
    chk("2.1 list returns 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    items = (r.json() or {}).get("items", []) if r.status_code == 200 else []
    chk("2.1 list non-empty", len(items) >= 1, f"len={len(items)}")
    chk("2.1 every row has dashboard_full_access",
        all("dashboard_full_access" in it for it in items),
        f"sample={items[:1]}")
    sagar_row = next((it for it in items if it.get("user_id") == sagar_uid), None)
    chk("2.1 sagar row found", sagar_row is not None, f"items={items}")
    if sagar_row:
        chk("2.1 sagar dashboard_full_access=true (default)",
            sagar_row.get("dashboard_full_access") is True,
            f"row={sagar_row}")


