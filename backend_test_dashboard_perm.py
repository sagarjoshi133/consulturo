def test_4_authorization(sagar_uid, doctor_uid):
    print("\n=== Test 4: Authorization on PATCH dashboard-perm ===")

    # 10. primary_owner (sagar) → 403
    r = requests.patch(
        f"{API}/admin/primary-owners/{sagar_uid}/dashboard-perm",
        headers=H(OWNER_TOKEN), json={"dashboard_full_access": False}, timeout=15,
    )
    chk("4.10 sagar PATCH → 403", r.status_code == 403, f"st={r.status_code} body={r.text[:300]}")

    # 11. doctor → 403
    r = requests.patch(
        f"{API}/admin/primary-owners/{sagar_uid}/dashboard-perm",
        headers=H(DOCTOR_TOKEN), json={"dashboard_full_access": False}, timeout=15,
    )
    chk("4.11 doctor PATCH → 403", r.status_code == 403, f"st={r.status_code} body={r.text[:300]}")

    # 12. PATCH on a non-primary_owner target → 400
    if doctor_uid:
        r = requests.patch(
            f"{API}/admin/primary-owners/{doctor_uid}/dashboard-perm",
            headers=H(SO_TOKEN), json={"dashboard_full_access": False}, timeout=15,
        )
        chk("4.12 PATCH non-primary_owner target → 400",
            r.status_code == 400, f"st={r.status_code} body={r.text[:300]}")
        if r.status_code == 400:
            detail = (r.json() or {}).get("detail", "")
            chk("4.12 detail mentions primary_owner",
                "primary_owner" in detail.lower() or "primary owner" in detail.lower(),
                f"detail={detail!r}")
    else:
        chk("4.12 (skipped) doctor uid not resolvable", False, "doctor uid empty")

    # 13. PATCH on non-existent user → 404
    r = requests.patch(
        f"{API}/admin/primary-owners/does_not_exist_user_xyz/dashboard-perm",
        headers=H(SO_TOKEN), json={"dashboard_full_access": False}, timeout=15,
    )
    chk("4.13 PATCH unknown user_id → 404", r.status_code == 404, f"st={r.status_code} body={r.text[:300]}")


