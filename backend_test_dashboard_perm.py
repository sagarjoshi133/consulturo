def test_1_defaults():
    print("\n=== Test 1: /api/me/tier defaults ===")

    # 1.1 sagar (primary_owner, no DB flag) → true (default-true rule)
    r = requests.get(f"{API}/me/tier", headers=H(OWNER_TOKEN), timeout=15)
    chk("1.1 sagar /me/tier returns 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    body = r.json() if r.status_code == 200 else {}
    chk("1.1 sagar role=primary_owner", body.get("role") == "primary_owner", f"body={body}")
    chk("1.1 sagar dashboard_full_access default-true",
        body.get("dashboard_full_access") is True,
        f"got={body.get('dashboard_full_access')!r}")

    # 1.2 super_owner → true
    r = requests.get(f"{API}/me/tier", headers=H(SO_TOKEN), timeout=15)
    chk("1.2 super_owner /me/tier 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    body = r.json() if r.status_code == 200 else {}
    chk("1.2 super_owner role=super_owner", body.get("role") == "super_owner", f"body={body}")
    chk("1.2 super_owner is_super_owner=true", body.get("is_super_owner") is True, f"body={body}")
    chk("1.2 super_owner dashboard_full_access=true",
        body.get("dashboard_full_access") is True,
        f"got={body.get('dashboard_full_access')!r}")

    # 1.3 doctor → false (non owner-tier, per-user prop unset)
    r = requests.get(f"{API}/me/tier", headers=H(DOCTOR_TOKEN), timeout=15)
    chk("1.3 doctor /me/tier 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    body = r.json() if r.status_code == 200 else {}
    chk("1.3 doctor role=doctor", body.get("role") == "doctor", f"body={body}")
    chk("1.3 doctor dashboard_full_access=false",
        body.get("dashboard_full_access") is False,
        f"got={body.get('dashboard_full_access')!r}")


