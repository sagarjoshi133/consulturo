def test_3_patch_lifecycle(sagar_uid):
    print("\n=== Test 3: PATCH /api/admin/primary-owners/{id}/dashboard-perm ===")

    # 5. Flip OFF
    r = requests.patch(
        f"{API}/admin/primary-owners/{sagar_uid}/dashboard-perm",
        headers=H(SO_TOKEN), json={"dashboard_full_access": False}, timeout=15,
    )
    chk("3.5 PATCH false returns 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    body = r.json() if r.status_code == 200 else {}
    chk("3.5 response dashboard_full_access=false",
        body.get("dashboard_full_access") is False, f"body={body}")

    # 6. /me/tier reflects false
    r = requests.get(f"{API}/me/tier", headers=H(OWNER_TOKEN), timeout=15)
    body = r.json() if r.status_code == 200 else {}
    chk("3.6 sagar /me/tier dashboard_full_access=false",
        body.get("dashboard_full_access") is False, f"body={body}")

    # 7. List shows false for sagar
    r = requests.get(f"{API}/admin/primary-owners", headers=H(SO_TOKEN), timeout=15)
    items = (r.json() or {}).get("items", []) if r.status_code == 200 else []
    sagar_row = next((it for it in items if it.get("user_id") == sagar_uid), None)
    chk("3.7 list now sagar dashboard_full_access=false",
        sagar_row is not None and sagar_row.get("dashboard_full_access") is False,
        f"row={sagar_row}")

    # 8. Flip back ON
    r = requests.patch(
        f"{API}/admin/primary-owners/{sagar_uid}/dashboard-perm",
        headers=H(SO_TOKEN), json={"dashboard_full_access": True}, timeout=15,
    )
    chk("3.8 PATCH true returns 200", r.status_code == 200, f"st={r.status_code} body={r.text[:300]}")
    body = r.json() if r.status_code == 200 else {}
    chk("3.8 response dashboard_full_access=true",
        body.get("dashboard_full_access") is True, f"body={body}")

    # 9. /me/tier reflects true
    r = requests.get(f"{API}/me/tier", headers=H(OWNER_TOKEN), timeout=15)
    body = r.json() if r.status_code == 200 else {}
    chk("3.9 sagar /me/tier dashboard_full_access=true",
        body.get("dashboard_full_access") is True, f"body={body}")


